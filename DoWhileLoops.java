
public class DoWhileLoops {

	public static void main(String[] args) {
		int counter = 15;
		
		do // does this FIRST... used any time you wanna do something AT LEAST ONCE.
		{
			System.out.println(counter);
			counter++;
			
		}
		
		while (counter < 10); // ... CHECKS THIS AFTER THE "DO" IS DONE.

	}

}
