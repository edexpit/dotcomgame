
public class Lesson25 {

	public static void main(String[] args) {
		System.out.println(Math.abs(-26.7)); // absolute value
		
		System.out.println(Math.ceil(7.8)); //rounds up
		
		System.out.println(Math.floor(7.8)); //rounds down);
		
		System.out.println(Math.max(8.6, 5.2)); //greater of two numbers
		
		System.out.println(Math.min(8.6, 5.2)); //lower of 2 numbers
		
		System.out.println(Math.pow(5, 3)); // exponent ... 5 to the3rd power
		
		System.out.println(Math.sqrt(9));  //square root

	}

}
